public class PoorDog {

    private int size;
    private String name;

    // Setter method for size
    public void setSize(int size) {
        this.size = size;
    }

    // Getter method for size
    public int getSize() {
        return size;
    }

    // Setter method for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for name
    public String getName() {
        return name;
    }
}

