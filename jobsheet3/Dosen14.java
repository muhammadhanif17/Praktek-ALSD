public class Dosen14 {
    //Deklarasi atribut
    String kode;
    String nama;
    boolean jenisKelamin; //true : pria, false = wanita
    int usia;

    //Konstruktor berparameter
    public Dosen14(String kode, String nama, boolean jenisKelamin, int usia){
        this.kode = kode;
        this.nama = nama;
        this.jenisKelamin = jenisKelamin;
        this.usia = usia;
    }
}